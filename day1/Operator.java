package day1;

public class Operator {
    public static void main(String[] args) {
//  연산자
//        산술연산자
//        +, -, *, /, %
        System.out.println(10 + 5);
        System.out.println(10 - 5);
        System.out.println(10 * 5);
        System.out.println(10 / 5);  // 2 (나눈 몫)
        System.out.println(10 % 5);  // 0 (나눈 나머지값)
//  비교연산자
        // >, <, >=, <=, ==, !==
        // true, false -> 논리값(boolean)으로 결과가 나온다.
        System.out.println(10 > 5);
        System.out.println(10 < 5);

    }
}
